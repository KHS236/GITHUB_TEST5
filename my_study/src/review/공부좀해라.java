package review;

public class 공부좀해라 {

	public static void main(String[] args) {
		
		
		
		
		
		
		
		
		
		
		
		
		// 은행 대기번호 표
		int waiting = 0;
		System.out.println("대기 인원 : " + waiting++ + " 명"); // 대기 인원 : 0명
		System.out.println("대기 인원 : " + waiting++ + " 명"); // 대기 인원 : 1명 
//													(위에서 0명을 출력하고 1을 더했음)
		System.out.println("대기 인원 : " + waiting++ + " 명"); // 대기 인원 : 2명 
		
		System.out.println("총 대기 인원 : " + waiting + " 명"); 
		// 총 대기 인원 : 3명 (마찬가지로 위에서 출력되고 연산된 종합값이 나옴
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
